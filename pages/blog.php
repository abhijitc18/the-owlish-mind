    const items = document.querySelectorAll('.faq-item');

    items.forEach(item => {
        const question = item.querySelector('.faq-question');
        question.addEventListener('click', () => {
            items.forEach(i => i !== item && i.classList.remove('active'));
            item.classList.toggle('active');
        });
    });
});
