<section class="faq-section" style="padding: 20px 20px 60px 20px; background: #f7f7f7;">
    <div class="container">
        <h2 class="section-title" style="text-align: center; margin-bottom: 40px;">FAQ's - Counselling & Therapy</h2>

        <h3 class="faq-subheading">
            <i class="fa fa-star"></i>Understanding Counselling & Therapy
        </h3>
        <div class="faq-item">
            <div class="faq-question">1. What is the difference between counselling and therapy?</div>
            <div class="faq-answer">
                <ul>
                    <li>
                        Counselling focuses on short-term issues like stress, relationship conflicts, or career
                        concerns.
                    </li>
                    <li>
                        Therapy (psychotherapy) is a more in-depth process that explores emotions, behaviors, and past
                        experiences, often addressing long-term concerns like anxiety, depression, or trauma.
                    </li>
                </ul>
            </div>
        </div>

        <div class="faq-item">
            <div class="faq-question">2. Who can benefit from counselling or therapy?</div>
            <div class="faq-answer">
                <p>
                    Anyone facing emotional distress, mental health concerns, or life transitions can benefit,
                    including:
                </p>
                <ul>
                    <li>Individuals dealing with anxiety, stress, or trauma.</li>
                    <li>Couples struggling with relationship issues.</li>
                    <li>Students facing academic or career-related stress.</li>
                    <li>Professionals experiencing burnout or workplace pressure.</li>
                </ul>
            </div>
        </div>

        <h3 class="faq-subheading">
            <i class="fa fa-star"></i>The Therapy Process
        </h3>

        <div class="faq-item">
            <div class="faq-question">3. What happens in a typical therapy session?</div>
            <div class="faq-answer">
                <ul>
                    <li>
                        A confidential conversation about concerns in a safe, non-judgmental space.
                    </li>
                    <li>
                        Identifying triggers, thought patterns, and coping mechanisms.
                    </li>
                    <li>
                        Learning strategies to handle emotions and life challenges.
                    </li>
                    <li>
                        Setting personal growth and emotional well-being goals.
                    </li>
                </ul>
            </div>
        </div>

        <div class="faq-item">
            <div class="faq-question">4. How many sessions will I need?</div>
            <div class="faq-answer">
                <p>
                    The number varies. Some issues may require 6-8 sessions, while deeper concerns may need
                    long-term therapy over several months. The therapist will tailor a plan based on progress.
                </p>
            </div>
        </div>

        <div class="faq-item">
            <div class="faq-question">5. Is online therapy effective?</div>
            <div class="faq-answer">
                <p>
                    Yes. Research shows that online therapy via video calls or chat can be as effective as
                    in-person therapy for most mental health concerns, providing convenience and accessibility.
                </p>
            </div>
        </div>

        <h3 class="faq-subheading">
            <i class="fa fa-star"></i>Confidentiality & Privacy
        </h3>

        <div class="faq-item">
            <div class="faq-question">6. Are therapy sessions confidential?</div>
            <div class="faq-answer">
                <p>
                    Yes, therapists follow strict ethical guidelines to ensure privacy. However, confidentiality
                    may be broken if:
                </p>
                <ul>
                    <li>
                        There is a risk of harm to yourself or others.
                    </li>
                    <li>
                        A legal obligation requires disclosure (e.g., abuse cases or court orders).
                    </li>
                </ul>
            </div>
        </div>

        <h3 class="faq-subheading">
            <i class="fa fa-star"></i>Concerns About Therapy
        </h3>
        <div class="faq-item">
            <div class="faq-question">7. What if I feel uncomfortable opening up in therapy?</div>
            <div class="faq-answer">
                <p>
                    It’s natural to feel hesitant. A good therapist will create a safe, supportive space where
                    you can share at your own pace. If you don’t feel comfortable, you can switch therapists.
                </p>
            </div>
        </div>

        <div class="faq-item">
            <div class="faq-question">8. Does seeking therapy mean I have a mental illness?</div>
            <div class="faq-answer">
                <p>
                    No. Therapy is for everyone, including those managing stress, improving relationships, or
                    seeking personal growth. Just like physical health check-ups, therapy helps maintain
                    emotional well-being.
                </p>
            </div>
        </div>

        <h3 class="faq-subheading">
            <i class="fa fa-star"></i>Therapy for Specific Needs
        </h3>
        <div class="faq-item">
            <div class="faq-question">9. Can therapy help with workplace stress and burnout?</div>
            <div class="faq-answer">
                <p>
                    Yes. Therapy provides tools to manage job-related anxiety, improve work-life balance, and
                    enhance productivity. Many corporate wellness programs also offer mental health support.
                </p>
            </div>
        </div>

        <div class="faq-item">
            <div class="faq-question">10. When should I stop therapy?</div>
            <div class="faq-answer">
                <p>
                    You can stop therapy when:
                </p>
                <ul>
                    <li>
                        You’ve achieved your personal or emotional well-being goals.
                    </li>
                    <li>
                        You feel more self-aware and can manage challenges effectively.
                    </li>
                    <li>
                        You and your therapist agree that sessions are no longer needed.
                    </li>
                </ul>
            </div>
        </div>

        <p class="mt-4" style="color: #46b7de;">
            <i class="fa fa-thumb-tack"></i><strong>Still have questions? Reach out to us for guidance!</strong>
        </p>
    </div>
</section>

